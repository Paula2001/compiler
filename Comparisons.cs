class Comparisons
{
    public static void Main()
    {
        int a = 5;
        int b = 3;
        int c = 5;

        Console.WriteLine(a == b); // False
        Console.WriteLine(a == c); // True
        Console.WriteLine(a != b); // True
        Console.WriteLine(a != c); // False
        Console.WriteLine(a > b); // True
        Console.WriteLine(a < b); // False
        Console.WriteLine(a >= b); // True
        Console.WriteLine(a <= b); // False
    }
}